vim.opt.clipboard = "unnamedplus"

vim.opt.number = true
vim.opt.relativenumber = false
vim.opt.spelllang = { "ru", "en" }

vim.api.nvim_create_autocmd("FileType", {
  pattern = "cpp",
  callback = function()
    vim.opt_local.tabstop = 4
    vim.opt_local.shiftwidth = 4
    vim.opt_local.expandtab = true
  end,
})

